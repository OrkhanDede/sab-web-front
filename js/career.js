document.addEventListener("DOMContentLoaded", () => {
  const closeDialog = document.querySelectorAll(".closeDialog");
  const vacationDialog = document.getElementById("vacationDialog");

  vacations.forEach((vacation) => {
    appendVacations(vacation);
  });

  closeDialog.forEach((closeItem) => {
    closeItem.addEventListener("click", () => {
      vacationDialog.style.display = "none";
      document.body.style.overflowY = "auto";
      resetUploadBtn();
    });
  });
});

function openVacation(vacation) {
  const vacationDialog = document.getElementById("vacationDialog");
  const title = document.querySelector("#vacationDialog #title");
  const content = document.querySelector("#vacationDialog #content");
  const deadline = document.querySelector("#vacationDialog #deadline");
  const lang = localStorage.getItem("language") === "az" ? "az" : "en";

  if (vacationDialog.style.display === "none") {
    vacationDialog.style.display = "flex";
    title.textContent = vacation.title[lang];
    content.innerHTML = vacation.content[lang];
    deadline.textContent = vacation.deadline;
    document.body.style.overflowY = "hidden";
  }
}

function uploadCV() {
  const uploadBtn = document.querySelector("#uploadCV");
  const thankYouBtn = document.querySelector("#thankYouBtn");
  uploadBtn.style.display = "none";
  thankYouBtn.style.display = "flex";
}

function resetUploadBtn() {
  const uploadBtn = document.querySelector("#uploadCV");
  const thankYouBtn = document.querySelector("#thankYouBtn");
  uploadBtn.style.display = "flex";
  thankYouBtn.style.display = "none";
}

function appendVacations(vacation) {
  const lang = localStorage.getItem("language") === "az" ? "az" : "en";
  const container = document.getElementById("vacationsContainer");
  const element = document.createElement("div");
  element.classList.add(
    "bg-white",
    "lg:rounded-3xl",
    "rounded-[3rem]",
    "px-10",
    "lg:py-7",
    "py-[3rem]",
    "flex",
    "flex-col",
    "lg:gap-0",
    "gap-[2rem]",
    "justify-between",
    "h-full",
    "lg:aspect-video"
  );

  element.setAttribute("vacation-data", vacation);

  const title = document.createElement("h5");
  title.classList.add("text-[3rem]", "leading-[1.3em]", "font-[600]", "pb-3");
  title.textContent = vacation.title[lang];
  element.appendChild(title);

  const button = document.createElement("button");
  button.classList.add(
    "border",
    "rounded-full",
    "lg:px-5",
    "px-8",
    "more_btn",
    "w-max",
    "business-brands",
    "font-semibold",
    "lg:h-[4rem]",
    "h-[5rem]",
    "lg:text-[1.3rem]",
    "text-[2rem]",
    "transition-colors",
    "text-secondary",
    "hover:bg-primary",
    "hover:border-primary",
    "hover:text-white",
    "transition-colors",
    "border-secondary",
    "flex",
    "gap-2",
    "justify-center",
    "items-center"
  );
  const span = document.createElement("span");
  span.setAttribute("data-translate", "learn_more_btn");
  button.appendChild(span);

  const img = document.createElement("img");
  img.classList.add("w-6");
  img.setAttribute("src", "/assets/icons/arrow-right.svg");
  img.setAttribute("alt", "arrow-right");
  button.appendChild(img);

  button.addEventListener("click", function () {
    openVacation(vacation);
  });

  element.appendChild(button);
  container.appendChild(element);
}
